# Define the following variables
# name, last_name, species, eye_color, hair_color

name = 'Steph'
last_name = 'Robinson'
species = 'giraffe'
eye_colour = 'Green'
hair_colour = 'Blonde'
age = 23

# Prompt user for input and Re-assign these
name = input('What new name would you like to have?')
